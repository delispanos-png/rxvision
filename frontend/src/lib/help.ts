export function helpFor(pathname: string): Help | null {
  const keys = Object.keys(HELP).sort((a, b) => b.length - a.length);
  const k = keys.find((key) => pathname === key || pathname.startsWith(key + "/"));
  return k ? HELP[k] : null;
}
